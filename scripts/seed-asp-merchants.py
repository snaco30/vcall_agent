CSV_PATH = os.path.join(ROOT, "data", "asp_merchant_usage.csv")


def write_csv() -> None:
    os.makedirs(os.path.dirname(CSV_PATH), exist_ok=True)
    with open(CSV_PATH, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["거래처(상호)", "ASP ID", "신청일자", "비고"])
        for name, asp_id, join_date in ROWS:
            writer.writerow([name, asp_id, join_date, ""])


def main() -> int:
    write_csv()
    result = import_asp_csv(os.path.join(ROOT, "data", "asp_merchant_usage.csv"), replace=True)
    print(result.get("message", result))
    print(f"CSV: {CSV_PATH}")
    print(f"imported={result.get('imported')} skipped={result.get('skipped')}")
    return 0 if result.get("ok") else 1


